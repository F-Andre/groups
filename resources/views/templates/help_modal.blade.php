        <p class="h5">Qu'est-ce "Groups"?</p>
        <p>
          <ul>
            <li>"Groups" est une plateforme de groupes de discussions fermés respectueuse des données personnelles, du
              droit à l'oubli et sans publicité.</li>
            <li>"Groups" n'est pas un réseau social, il n'y a pas de profil utilisateur. Les autres membres d'un groupe ne verront que votre pseudo et votre avatar.</li>
          </ul>
        </p>
        <p class="h5">Comment ça marche?</p>
        <p>
          <ul>
            <li>
              En créant un compte sur le site, vous avez la possibilité de rejoindre ou de créer un groupe de
              discussion.
            </li>
            <li>
              Le créateur d'un groupe dispose des droits d'administration de ce groupe. C'est à dire qu'il a la
              possibilité de supprimer des articles et des commentaires. Il peut aussi désigner un administrateur
              parmi
              les membre du groupe. L'administrateur du groupe a aussi la faculté d'envoyer des avertissements et
              de radier un membre du groupe.
            </li>
            <li>
              Quand un membre quitte le groupe, tout ses articles, images et commentaires sont aussi supprimés
              définitivement.
            </li>
            <li>
              Les publications ne doivent pas porter atteinte aux autres utilisateurs. "Groups" se réserve un droit de regard sur tous les
              contenus publiés sur la plateforme.
            </li>
          </ul>
        </p>
        <p class="h5">Respect des données personnelles:</p>
        <p>
          <ul>
            <li>"Groups" ne donne, n'échange, ni ne vend les données des utilisateurs inscrits. Les données sont
              stockées sur des serveurs situés en France.</li>
            <li>"Groups" n'utilise pas d'outils de fournisseurs tiers captant des données comme par exemple des outils
              d'analyse Google ou Facebook.</li>
            <li>Vous pouvez connaître à tout moment le contenu des données vous concernant en en faisant la demande.</li>
          </ul>
        </p>
        <p class="h5">Le droit à l'oubli:</p>
        <p>
          <ul>
            <li>La suppression d'un compte entraine automatiquement la suppression définitive de toutes les données
              liées à ce compte (données du compte, articles, photos et commentaires).</li>
            <li>Aucune donnée d'un compte supprimé n'est conservée sur les serveurs.</li>
          </ul>
        </p>
